SELECT 
    c.city AS city_name,c.city_id,
    y.year AS year_value,y.year_id,

    -- Employee Employment Data
    eev.employment_id,
    ee.employment_characteristics,
    eev.value AS employment_value,

    -- Cost of Living Data
	col.cost_of_living_id,
    col.cost_of_living_items,
    colc.cost_of_living_category,
    col.price_in_cad,

    -- Commute Data
    com.value AS commute_time

FROM public.col_city c
JOIN public.col_year y ON 1=1  -- Cross join ensures all years are considered

-- Join Employee Employment Fact Table
JOIN public.employee_employment_value eev 
    ON eev.city_id = c.city_id AND eev.year_id = y.year_id
JOIN public.employee_employment ee 
    ON eev.employment_id = ee.employment_id

-- Join Cost of Living Fact Table
JOIN public.col_cost_of_living col 
    ON col.city_id = c.city_id AND col.year_id = y.year_id
	JOIN public.col_cost_of_living_category colc 
    ON col.cost_of_living_category_id = colc.cost_of_living_category_id

-- Join Commute Table
JOIN public.commute com 
    ON com.city_id = c.city_id AND com.year_id = y.year_id;






